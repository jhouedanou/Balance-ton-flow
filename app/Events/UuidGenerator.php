<?php
namespace App\Events;

use Webpatser\Uuid\Uuid;

trait UuidGenerator
{

    /**
     * Boot function from laravel.
     */
    protected static function boot()
    {
        parent::boot();

        static::creating(function ($model) {
            $model->{$model->getKeyName()} = Uuid::generate()->string;
        });
    }
}
